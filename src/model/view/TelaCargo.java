    private javax.swing.JCheckBox chkFornecedores;
    private javax.swing.JCheckBox chkProdutos;
    private javax.swing.JCheckBox chkUsuarios;
    private javax.swing.JCheckBox chkVendas;
    private javax.swing.JLabel jLabel1;
    private javax.swing.JLabel jLabel2;
    private javax.swing.JLabel jLabel3;
    private javax.swing.JPanel jPanel1;
    private javax.swing.JPanel jPanel2;
    private javax.swing.JPanel jPanel3;
    private javax.swing.JScrollPane jScrollPane1;
    private javax.swing.JTextField txtCargo;
    private javax.swing.JLabel txtRegistros;
