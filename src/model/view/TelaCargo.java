    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(!StringUtils.isNullOrEmpty(txtCargo.getText().trim())){
            Cargo cargo = new Cargo();
            cargo.setCargo(txtCargo.getText().trim());
            cargo.setPermissoes(definePermisoes());
            CargoDAO dao = new CargoDAO();
                if(!this.editando){
                    if(dao.adicionaCargo(cargo)){
                        cargo.setIdCargo(dao.getLastCodeCargo());
                        this.lista.add(cargo);
                        ultimo();
                        habilitaBotoes();
                    }
                } else {
                    cargo.setIdCargo(this.lista.get(this.posicao).getIdCargo());
                    if(dao.atualizaCargo(cargo)){
                        this.lista.set(this.posicao, cargo);
                        controlaLista();
                        this.editando = false;
                        habilitaBotoes();
                    }
                }
        } else JOptionPane.showMessageDialog(null, "Há campos obrigatórios que não foram preenchidos!", "Caixa de Diálogo", 0);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Você quer mesmo excluir este registro?", "Caixa de Confirmação", 0)==0){
            Cargo cargo = this.lista.get(this.posicao);
            CargoDAO dao = new CargoDAO();
            if(dao.removeCargo(cargo)){
                this.lista.remove(cargo);
                    if(this.posicao<this.lista.size()){
                        this.posicao--;
                        proximo();
                    } else anterior();
            }
        }   
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void txtCargoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCargoKeyTyped
        if(!Character.isAlphabetic(evt.getKeyChar()) && !Character.isSpaceChar(evt.getKeyChar()) && !".-".contains(evt.getKeyChar()+""))
            evt.consume();
        
        if(txtCargo.getText().length()>=25)
            evt.consume();
    }//GEN-LAST:event_txtCargoKeyTyped

    private void btnVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoltarActionPerformed
        if(txtCargo.isEditable()){
            if(JOptionPane.showConfirmDialog(this, "Você quer mesmo desfazer as alterações deste registro?", "Caixa de Confirmação", 0)==0){
                habilitaBotoes();
                this.posicao--;
                proximo();
            }   
        } else if(JOptionPane.showConfirmDialog(this, "Você quer mesmo voltar à tela anterior?", "Caixa de Confirmação", 0)==0)
                this.dispose();
    }//GEN-LAST:event_btnVoltarActionPerformed

