    private void carregaLista(){
        CargoDAO dao = new CargoDAO();
        this.lista = dao.getListaCargo();
        habilitaBotoes();
        primeiro();
    }
    
    private void primeiro (){
        this.posicao = 0;
        controlaLista();
    }
    
    private void anterior (){
        this.posicao--;
        controlaLista();
    }
    
    private void proximo (){
        this.posicao++;
        controlaLista();
    }
    
    private void ultimo (){
        this.posicao = this.lista.size()-1;
        controlaLista();
    }
    
    private void exibirInformacoes (){
        Cargo cargo = this.lista.get(this.posicao);
        txtCargo.setText(cargo.getCargo());
        chkUsuarios.setSelected(false);
        chkClientes.setSelected(false);
        chkProdutos.setSelected(false);
        chkEmpresas.setSelected(false);
        exibePermissoes(cargo.getPermissoes());
    }
    
    private void controlaLista(){
        if(lista.isEmpty()){
            txtCargo.setText(null);
            chkUsuarios.setSelected(false);
            chkClientes.setSelected(false);
            chkProdutos.setSelected(false);
            chkEmpresas.setSelected(false);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            desabilitaCampos();
            btnAnterior.setEnabled(false);
            btnProximo.setEnabled(false);
            btnUltimo.setEnabled(false);
            btnPrimeiro.setEnabled(false);
            txtRegistros.setText("NÃO HÁ REGISTROS");
        } else {
            txtRegistros.setText("REGISTRO " + (this.posicao+1) + " DE " + (this.lista.size()));
                if(this.posicao<=0){
                    btnPrimeiro.setEnabled(false);
                    btnAnterior.setEnabled(false);
                } else {
                    btnPrimeiro.setEnabled(true);
                    btnAnterior.setEnabled(true);
                }
                if(this.posicao==this.lista.size()-1){
                    btnProximo.setEnabled(false);
                    btnUltimo.setEnabled(false);
                } else {
                    btnProximo.setEnabled(true);
                    btnUltimo.setEnabled(true);
                }
            exibirInformacoes();
        }
    }
    
    private void habilitaBotoes (){
        desabilitaCampos();
        btnNovo.setEnabled(true);
        btnSalvar.setEnabled(false);
        btnEditar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }
    
    private void desabilitaBotoes (){
        habilitaCampos();
        btnNovo.setEnabled(false);
        btnSalvar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnPrimeiro.setEnabled(false);
        btnAnterior.setEnabled(false);
        btnProximo.setEnabled(false);
        btnUltimo.setEnabled(false);
    }
    
    private String definePermisoes(){
        String permissoes = "";
            if (chkVendas.isSelected())
                permissoes += "VD";
            if (chkCompras.isSelected())
                permissoes += "CP";
            if(chkClientes.isSelected())
                permissoes += "CL";
            if(chkEmpresas.isSelected())
                permissoes += "EP";
            if(chkFornecedores.isSelected())
                permissoes += "FO";
            if(chkProdutos.isSelected())
                permissoes += "PD";
            if(chkUsuarios.isSelected())
                permissoes += "US";
        return permissoes;
    }
    
    private void exibePermissoes(String permissoes){
            for(int i = 0;i<permissoes.length();i+=2){
                if (permissoes.substring(i, i+2).equals("VD"))
                    chkVendas.setSelected(true);
                else if (permissoes.substring(i, i+2).equals("CP"))
                    chkCompras.setSelected(true);
                else if (permissoes.substring(i, i+2).equals("CL"))
                    chkClientes.setSelected(true);
                else if (permissoes.substring(i, i+2).equals("EP"))
                    chkEmpresas.setSelected(true);
                else if(permissoes.substring(i, i+2).equals("FO"))
                    chkFornecedores.setSelected(true);
                else if (permissoes.substring(i, i+2).equals("PD"))
                    chkProdutos.setSelected(true);
                else if(permissoes.substring(i, i+2).equals("US"))
                    chkUsuarios.setSelected(true);
            }
    }
    
    private void habilitaCampos(){
        txtCargo.setEditable(true);
        chkVendas.setEnabled(true);
        chkCompras.setEnabled(true);
        chkClientes.setEnabled(true);
        chkEmpresas.setEnabled(true);
        chkFornecedores.setEnabled(true);
        chkProdutos.setEnabled(true);
        chkUsuarios.setEnabled(true);
        txtCargo.requestFocus();
    }
    
    private void desabilitaCampos(){
        txtCargo.setEditable(false);
        chkVendas.setEnabled(false);
        chkCompras.setEnabled(false);
        chkClientes.setEnabled(false);
        chkEmpresas.setEnabled(false);
        chkFornecedores.setEnabled(false);
        chkProdutos.setEnabled(false);
        chkUsuarios.setEnabled(false);
        txtRegistros.requestFocus();
    }
    
    private void limpaCampos(){
        desabilitaBotoes();        
        txtCargo.setText(null);
        chkVendas.setSelected(false);
        chkCompras.setSelected(false);
        chkClientes.setSelected(false);
        chkEmpresas.setSelected(false);
        chkFornecedores.setSelected(false);
        chkProdutos.setSelected(false);
        chkUsuarios.setSelected(false);
        txtRegistros.setText(" ");
        txtCargo.requestFocus();
    }
