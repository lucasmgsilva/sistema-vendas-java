    private void verificaPermissoes(){
        String permissoes = Sessao.getInstance().getUsuario().getCargo().getPermissoes();
        for(int i=0;i<permissoes.length()-1;i++){
            if (permissoes.substring(i, i+2).equals("VD")){
                btnVendas.setEnabled(true);
                mnVendas.setEnabled(true);
            } else if (permissoes.substring(i, i+2).equals("CP")){
                btnCompras.setEnabled(true);
                mnCompras.setEnabled(true);
            } else if (permissoes.substring(i, i+2).equals("CL")){
                btnClientes.setEnabled(true);
                mnPrincipal.setEnabled(true);
                mnCliente.setEnabled(true);
            } else if (permissoes.substring(i, i+2).equals("EP")){
                btnEmpresas.setEnabled(true);
                mnPrincipal.setEnabled(true);
                mnEmpresa.setEnabled(true);
            } else if (permissoes.substring(i, i+2).equals("FO")){
                btnFornecedores.setEnabled(true);
                mnPrincipal.setEnabled(true);
                mnFornecedor.setEnabled(true);
            } else if (permissoes.substring(i, i+2).equals("PD")){
                btnProdutos.setEnabled(true);
                mnPrincipal.setEnabled(true);
                mnBase.setEnabled(true);
                mnProduto.setEnabled(true);
                mnGrupoSubgrupo.setEnabled(true);
                mnMarca.setEnabled(true);
                mnUnidadeMedida.setEnabled(true);
            } else if (permissoes.substring(i, i+2).equals("US")){
                btnUsuarios.setEnabled(true);
                mnPrincipal.setEnabled(true);
                mnBase.setEnabled(true);
                mnUsuario.setEnabled(true);
                mnCargo.setEnabled(true);
            }
        } 
    }
    
