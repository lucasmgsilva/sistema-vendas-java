    private void btnExcluirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirClienteActionPerformed
        if(tblCliente.getSelectedRow()!=-1){
            if(JOptionPane.showConfirmDialog(this, "VocÃª quer mesmo excluir este registro?", "Caixa de ConfirmaÃ§Ã£o", 0)==0){
                if(clienteTableModel.getRow(tblCliente.getSelectedRow()) instanceof PessoaFisica){
                    PessoaFisicaDAO dao = new PessoaFisicaDAO();
                        if(dao.removePessoaFisica((PessoaFisica) clienteTableModel.getRow(tblCliente.getSelectedRow()))){
                            clienteTableModel.removeRow(clienteTableModel.getRow(tblCliente.getSelectedRow()));
                            JOptionPane.showMessageDialog(this, "Registro excluÃ­do com sucesso!", "Caixa de DiÃ¡logo", 1);
                        } else JOptionPane.showMessageDialog(null, "NÃ£o foi possÃ­vel excluir o registro selecionado.\nProvavelmente hÃ¡ vendas vinculadas a ele!", "Caixa de AfirmaÃ§Ã£o", 0);
                } else if(clienteTableModel.getRow(tblCliente.getSelectedRow()) instanceof PessoaJuridica){
                    PessoaJuridicaDAO dao = new PessoaJuridicaDAO();
                        if(dao.removePessoaJuridica((PessoaJuridica) clienteTableModel.getRow(tblCliente.getSelectedRow()))){
                            clienteTableModel.removeRow(clienteTableModel.getRow(tblCliente.getSelectedRow()));
                            JOptionPane.showMessageDialog(this, "Registro excluÃ­do com sucesso!", "Caixa de DiÃ¡logo", 1);
                        } else JOptionPane.showMessageDialog(null, "NÃ£o foi possÃ­vel excluir o registro selecionado.\nProvavelmente hÃ¡ vendas vinculadas a ele!", "Caixa de AfirmaÃ§Ã£o", 0);
                }
            }
        } else JOptionPane.showMessageDialog(null, "Nenhum registro foi selecionado!", "Caixa de AfirmaÃ§Ã£o", 0);
    }//GEN-LAST:event_btnExcluirClienteActionPerformed

