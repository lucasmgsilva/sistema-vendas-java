    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(!StringUtils.isNullOrEmpty(txtSigla.getText().trim()) && !StringUtils.isNullOrEmpty(txtDescricao.getText().trim())){
            UnidadeMedida unidadeMedida = new UnidadeMedida();
            unidadeMedida.setSigla(txtSigla.getText().trim().toUpperCase());
            unidadeMedida.setUnidadeMedida(txtDescricao.getText().trim());
            UnidadeMedidaDAO dao = new UnidadeMedidaDAO();
                if(!this.editando){
                    if(dao.adicionaUnidadeMedida(unidadeMedida)){
                        unidadeMedida.setIdUnidadeMedida(dao.getLastCodeUnidadeMedida());
                        this.lista.add(unidadeMedida);
                        ultimo();
                        habilitaBotoes();
                    }
                } else {
                    unidadeMedida.setIdUnidadeMedida(this.lista.get(this.posicao).getIdUnidadeMedida());
                    if(dao.atualizaUnidadeMedida(unidadeMedida)){
                        this.lista.set(this.posicao, unidadeMedida);
                        controlaLista();
                        this.editando = false;
                        habilitaBotoes();
                    }
                }
        } else JOptionPane.showMessageDialog(null, "Há campos obrigatórios que não foram preenchidos!", "Caixa de Diálogo", 0);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(JOptionPane.showConfirmDialog(this, "Você quer mesmo excluir este registro?", "Caixa de Confirmação", 0)==0){
            UnidadeMedida unidadeMedida = this.lista.get(this.posicao);
            UnidadeMedidaDAO dao = new UnidadeMedidaDAO();
                    if(dao.removeUnidadeMedida(unidadeMedida)){
                        this.lista.remove(unidadeMedida);
                            if(this.posicao<this.lista.size()){
                                this.posicao--;
                                proximo();
                            } else anterior();
                    }
        }   
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void txtSiglaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSiglaKeyTyped
        if(!Character.isAlphabetic(evt.getKeyChar()) && !Character.isSpaceChar(evt.getKeyChar()))
            evt.consume();
        
        if(Character.isLowerCase(evt.getKeyChar()))
            evt.setKeyChar(Character.toUpperCase(evt.getKeyChar()));

        if(txtSigla.getText().length()>=2)
            evt.consume();
    }//GEN-LAST:event_txtSiglaKeyTyped

    private void btnVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoltarActionPerformed
        if(txtSigla.isEditable()){
            if(JOptionPane.showConfirmDialog(this, "Você quer mesmo desfazer as alterações deste registro?", "Caixa de Confirmação", 0)==0){
                habilitaBotoes();
                this.posicao--;
                proximo();
            }   
        } else if(JOptionPane.showConfirmDialog(this, "Você quer mesmo voltar à tela anterior?", "Caixa de Confirmação", 0)==0)
                this.dispose();
    }//GEN-LAST:event_btnVoltarActionPerformed

