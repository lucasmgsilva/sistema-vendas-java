    public void submeterFormulario(){
        String senha = new String(txtSenha.getPassword());
            if(!StringUtils.isNullOrEmpty(txtUsuario.getText()) && !StringUtils.isNullOrEmpty(senha)){
                Usuario usuario = new Usuario();
                usuario.setLogin(txtUsuario.getText().trim());
                usuario.setSenha(senha.trim());
                
                UsuarioDAO dao = new UsuarioDAO();
                byte validacao = dao.validaUsuario(usuario);
                    if(validacao == 0){
                       TelaPrincipal principal = new TelaPrincipal();
                       principal.setExtendedState(MAXIMIZED_BOTH);
                       this.dispose();
                       principal.setVisible(true);
                       JOptionPane.showMessageDialog(this, "Olá, " + Sessao.getInstance().getUsuario().getNome().toUpperCase() + ".\nBem-vindo ao Sistema de Vendas!", "Caixa de Diálogo", 1);
                    } else if (validacao == -1){
                        txtUsuario.requestFocus();
                        txtUsuario.selectAll();
                        JOptionPane.showMessageDialog(this, "Usuário e/ou senha incorreta!", "Caixa de Diálogo", 0);
                    } else if (validacao == 1){
                        txtUsuario.requestFocus();
                        txtUsuario.selectAll();
                        JOptionPane.showMessageDialog(this, "Não foi possível acessar o sistema.\nEste usuário está indisponível!", "Caixa de Diálogo", 0);
                    }
            } else if (StringUtils.isNullOrEmpty(txtUsuario.getText()) && StringUtils.isNullOrEmpty(senha)){
                txtUsuario.requestFocus();
                JOptionPane.showMessageDialog(this, "Nenhum usuário e senha foi digitado!", "Caixa de Diálogo", 0);
            } else if (StringUtils.isNullOrEmpty(txtUsuario.getText())){
                txtUsuario.requestFocus();
                JOptionPane.showMessageDialog(this, "Nenhum usuário foi digitado!", "Caixa de Diálogo", 0);
            } else if (StringUtils.isNullOrEmpty(senha)){
                txtSenha.requestFocus();
                JOptionPane.showMessageDialog(this, "Nenhuma senha foi digitada!", "Caixa de Diálogo", 0);
            }
    }
    
    public void atualizarExibicaoSenha(){
        String senha = new String(txtSenha.getPassword());
            if(senha.isEmpty() || senha == null)
                txtExibirSenha.setText("Senha: Nenhuma");
            else txtExibirSenha.setText("Senha: " + senha);
    }
    
    private void checkExibirSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkExibirSenhaActionPerformed
        if(checkExibirSenha.isSelected()){
            atualizarExibicaoSenha();
            txtExibirSenha.setVisible(true);
        }
        
        if(!checkExibirSenha.isSelected()){
            txtExibirSenha.setVisible(false);
        }
    }//GEN-LAST:event_checkExibirSenhaActionPerformed

    private void txtSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyPressed
        if(evt.getKeyChar()==evt.VK_ENTER)
            submeterFormulario();
    }//GEN-LAST:event_txtSenhaKeyPressed

    private void txtUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyPressed
        if(evt.getKeyChar()==evt.VK_ENTER || evt.getKeyChar()==evt.VK_TAB){
            txtSenha.requestFocus();
            txtSenha.selectAll();
        }
    }//GEN-LAST:event_txtUsuarioKeyPressed

    private void btnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcessarActionPerformed
        submeterFormulario();
    }//GEN-LAST:event_btnAcessarActionPerformed

    private void txtUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyTyped
        if(!Character.isAlphabetic(evt.getKeyChar()) && !Character.isDigit(evt.getKeyChar()))
            evt.consume();
        
        if(txtUsuario.getText().length()>=20)
            evt.consume();
    }//GEN-LAST:event_txtUsuarioKeyTyped

    private void txtSenhaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyTyped
        if(!Character.isAlphabetic(evt.getKeyChar()) && !Character.isDigit(evt.getKeyChar()))
            evt.consume();
        
        if(new String(txtSenha.getPassword()).length()>=20)
            evt.consume();
    }//GEN-LAST:event_txtSenhaKeyTyped

    private void txtSenhaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyReleased
        if(checkExibirSenha.isSelected())
            atualizarExibicaoSenha();
    }//GEN-LAST:event_txtSenhaKeyReleased

    
